function changeColor1() {
    var elements = document.getElementsByClassName("colorchange");
    for(var i = 0; i < elements.length; i++) {
        elements[i].style.backgroundColor = "#9bedff";
    } 
    document.getElementById("name").style.color = "#9bedff"
    document.getElementById("footer").style.color = "#9bedff"
    document.getElementById("body").style.backgroundColor = "rgb(208, 244, 255)"

}

